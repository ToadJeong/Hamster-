/** 사전에 없으면 한국어 → 키 순으로 폴백 */
export function makeT(locale: Locale) {
  const base = DICT[locale] ?? DICT.ko;
  return (key: string, fallback?: string): string => base[key] ?? DICT.ko[key] ?? fallback ?? key;
}

export type TFn = ReturnType<typeof makeT>;
